class CharDemo{
	static char ch1;
	public static void main(String args[]){
		char ch = 'T';
		System.out.println("Char representation = " + ch);
		System.out.println("Unicode value of T = " + (int)ch);
		System.out.println("Size of character =" + Character.SIZE + "bits");
		System.out.println("Default value of character = " + ch1);	
	}
}